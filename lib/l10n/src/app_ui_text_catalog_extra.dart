String? extraUiText(String languageCode, String key, String? englishValue) {
  final values = switch (languageCode) {
    'de' => _deUiText,
    'ar' => _arUiText,
    _ => null,
  };
  if (values == null) return null;
  return values[key] ?? _localizedFallback(languageCode, key, englishValue);
}

String _localizedFallback(
  String languageCode,
  String key,
  String? englishValue,
) {
  final readableKey = key
      .replaceAllMapped(RegExp(r'([A-Z])'), (match) => ' ${match.group(1)}')
      .trim();
  return switch (languageCode) {
    'de' => 'Nicht übersetzt: $readableKey',
    'ar' => 'غير مترجم: $readableKey',
    _ => englishValue ?? key,
  };
}

